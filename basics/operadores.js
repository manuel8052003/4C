//Más operadores
let a = 10;
console.log(a);
a += 10; // a = a + 10

console.log(a);
a -= 10; // a = a - 10

console.log(a);
a *= 10; // a = a * 10

console.log(a);
a /= 10; // a = a / 10

console.log(a);
a %= 10; // a = a % 10

console.log(a);

//Operadores  relacionales
